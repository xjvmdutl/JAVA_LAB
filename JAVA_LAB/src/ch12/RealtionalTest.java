package ch12;

public class RealtionalTest {

	public static void main(String[] args) {
		int num1 = 5;
		int num2 = 3;
		boolean value = (num1 > num2);//관계연산자는 true,false만
		System.out.println(value);
		//비교 연산자
		System.out.println(num1 < num2);
		System.out.println(num1 >= num2);
		System.out.println(num1 <= num2);
		System.out.println(num1 == num2);
		System.out.println(num1 != num2);
		
	}

}
